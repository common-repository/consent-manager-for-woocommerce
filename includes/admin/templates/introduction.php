    <h2>Welcome to Consent Manager</h2>

    <p>This plugin allows you to manage the personal data of your customers or users. Especially for organisations that need to obtain consent from parents for their children. Currently it includes managing medical information, contact information and permissions to use customer photos and other content.</p>

    <p>Consent Manager is intended as a tool for managing GDPR compliance. It is best used for creating a single point where personal information storage, management and usage of data can be managed.</p>

    <p>On the right hand side is some information about Consent Manager including how to use it, who to contact if it isn't working as intended and where to make suggestions for future updates.</p>

    <hr/>

    <h3>What do the Different Tabs Mean?</h3>

    <p>The tabs shown above allows you collect, store and use data about the users on your site. Each tab relates to a different aspect of data and, together, formulate a holistic, GDPR- compliant approach to the way that personal data is utilised on your website.</p>

    <p>The functionality of the tabs can be broken down as follows:</p>

    <ol>
        <li><strong>Organisational Details:</strong> This tab is used to capture information about your website and the designated representative to contact as your business.</li>
        <li><strong>GDPR Policy Statements:</strong> As part of GDPR, businesses are required to report on the collection and use of data. Under this tab you'll draft statements on the data that you collect and how that data is used.</li>
        <li><strong>Policy Pages:</strong> This tab is where you'll include links to the various policies employed by your business. These include your privacy policy as well as your terms and conditions.</li>
        <li><strong>Consent Text:</strong> This tab details the text that will be presented to users when they consent to certain use of their data.</li>
    </ol>

